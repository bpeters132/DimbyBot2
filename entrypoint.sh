#!/bin/sh
set -e

echo "Bot Entrypoint: Starting..."

# ==============================================================================
# Generate lavaNodesConfig.js from Environment Variables
# ==============================================================================
# This uses environment variables passed into the container (e.g., from docker-compose)
# to create the configuration file needed by the bot to connect to Lavalink.

require_env() {
  var_name="$1"
  eval "var_value=\${$var_name-}"
  if [ -z "$var_value" ]; then
    echo "Bot Entrypoint: Missing required environment variable: $var_name" >&2
    return 1
  fi
}

validate_port() {
  case "$LAVALINK_PORT" in
    *[!0-9]* | "")
      echo "Bot Entrypoint: LAVALINK_PORT must be a numeric value, got '$LAVALINK_PORT'" >&2
      return 1
      ;;
  esac
}

validate_secure_flag() {
  case "$LAVALINK_SECURE" in
    true | false)
      ;;
    *)
      echo "Bot Entrypoint: LAVALINK_SECURE must be 'true' or 'false', got '$LAVALINK_SECURE'" >&2
      return 1
      ;;
  esac
}

require_env "LAVALINK_PASSWORD"
require_env "LAVALINK_HOST"
require_env "LAVALINK_PORT"
require_env "LAVALINK_NODE_ID"
require_env "LAVALINK_SECURE"
validate_port
validate_secure_flag

echo "Bot Entrypoint: Generating lavaNodesConfig.js..."
cat > lavaNodesConfig.js << EOF
// Generated by entrypoint.sh at container start
const nodes = [
  {
    authorization: "${LAVALINK_PASSWORD}",
    host: "${LAVALINK_HOST}",
    port: ${LAVALINK_PORT},
    id: "${LAVALINK_NODE_ID}",
    secure: ${LAVALINK_SECURE}
  },
];

export { nodes };
EOF
echo "Bot Entrypoint: lavaNodesConfig.js generated successfully."


# ==============================================================================
# Start the Bot Application
# ==============================================================================
# The 'exec' command replaces the current shell process with the 'yarn start' process.
# This ensures that 'yarn start' becomes the main process (PID 1) in the container,
# which is important for signal handling (like stopping the container).

if [ "$#" -gt 0 ]; then
  echo "Bot Entrypoint: Executing '$*'..."
  exec "$@"
fi

echo "Bot Entrypoint: Executing 'yarn start'..."
exec yarn start
