#!/bin/sh
set -e

echo "Bot Entrypoint: Starting..."

# ==============================================================================
# Generate lavaNodesConfig.js from Environment Variables
# ==============================================================================
# This uses environment variables passed into the container (e.g., from docker-compose)
# to create the configuration file needed by the bot to connect to Lavalink.

require_env() {
  var_name="$1"
  eval "var_value=\${$var_name-}"
  if [ -z "$var_value" ]; then
    echo "Bot Entrypoint: Missing required environment variable: $var_name" >&2
    return 1
  fi
}

validate_port() {
  case "$LAVALINK_PORT" in
    *[!0-9]* | "")
      echo "Bot Entrypoint: LAVALINK_PORT must be a numeric value, got '$LAVALINK_PORT'" >&2
      return 1
      ;;
    0)
      echo "Bot Entrypoint: LAVALINK_PORT must be greater than 0" >&2
      return 1
      ;;
    0*)
      echo "Bot Entrypoint: LAVALINK_PORT must not include leading zeroes, got '$LAVALINK_PORT'" >&2
      return 1
      ;;
  esac

  if [ "$LAVALINK_PORT" -gt 65535 ]; then
    echo "Bot Entrypoint: LAVALINK_PORT must be 65535 or lower, got '$LAVALINK_PORT'" >&2
    return 1
  fi
}

validate_secure_flag() {
  case "$LAVALINK_SECURE" in
    true | false)
      ;;
    *)
      echo "Bot Entrypoint: LAVALINK_SECURE must be 'true' or 'false', got '$LAVALINK_SECURE'" >&2
      return 1
      ;;
  esac
}

escape_js_string() {
  value="$1"
  VALUE="$value" node -e 'process.stdout.write(JSON.stringify(process.env.VALUE))'
}

require_env "LAVALINK_PASSWORD"
require_env "LAVALINK_HOST"
require_env "LAVALINK_PORT"
require_env "LAVALINK_NODE_ID"
require_env "LAVALINK_SECURE"
validate_port
validate_secure_flag

ESCAPED_LAVALINK_PASSWORD=$(escape_js_string "$LAVALINK_PASSWORD")
ESCAPED_LAVALINK_HOST=$(escape_js_string "$LAVALINK_HOST")
ESCAPED_LAVALINK_NODE_ID=$(escape_js_string "$LAVALINK_NODE_ID")

echo "Bot Entrypoint: Generating lavaNodesConfig.js..."
cat > lavaNodesConfig.js << EOF
// Generated by entrypoint.sh at container start
const nodes = [
  {
    authorization: ${ESCAPED_LAVALINK_PASSWORD},
    host: ${ESCAPED_LAVALINK_HOST},
    port: ${LAVALINK_PORT},
    id: ${ESCAPED_LAVALINK_NODE_ID},
    secure: ${LAVALINK_SECURE}
  },
];

export { nodes };
EOF
echo "Bot Entrypoint: lavaNodesConfig.js generated successfully."

# ==============================================================================
# Start the Bot Application
# ==============================================================================
# Next.js runs in a separate container (Dockerfile.web). This process is Discord + Express bot API only.

forward_shutdown() {
  if [ -n "${BOT_PID:-}" ] && kill -0 "$BOT_PID" >/dev/null 2>&1; then
    kill "$BOT_PID" >/dev/null 2>&1 || true
  fi
}

wait_for_bot() {
  set +e
  wait "$BOT_PID"
  BOT_EXIT_CODE=$?
  set -e
  exit "$BOT_EXIT_CODE"
}

# When the container user is root (e.g. one-off `docker run --user 0`), fix volume ownership
# then drop to `node`. Default image user is `node` (see Dockerfile) — then chown is skipped.
# Keep this shell as PID 1 so traps can forward shutdown to the bot child process.

if [ "$(id -u)" -eq 0 ]; then
  mkdir -p /app/storage
  chown -R node:node /app/storage 2>/dev/null || true
  mkdir -p /app/node_modules/.prisma /app/node_modules/@prisma
  chown -R node:node /app/node_modules/.prisma /app/node_modules/@prisma 2>/dev/null || true
  if [ "$#" -gt 0 ]; then
    trap forward_shutdown INT TERM
    echo "Bot Entrypoint: Executing '$*' as node..."
    su-exec node "$@" &
    BOT_PID=$!
    wait_for_bot
  fi
  trap forward_shutdown INT TERM
  echo "Bot Entrypoint: Executing 'yarn start' as node..."
  su-exec node yarn start &
  BOT_PID=$!
  wait_for_bot
fi

mkdir -p /app/storage /app/node_modules/.prisma /app/node_modules/@prisma
if [ "$#" -gt 0 ]; then
  trap forward_shutdown INT TERM
  echo "Bot Entrypoint: Executing '$*'..."
  "$@" &
  BOT_PID=$!
  wait_for_bot
fi

trap forward_shutdown INT TERM
echo "Bot Entrypoint: Executing 'yarn start'..."
yarn start &
BOT_PID=$!
wait_for_bot
